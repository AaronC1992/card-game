export const rulesText = `
# Filthy Minded Battle Deck — How To Play

## Age Requirement
This game is intended for players 18 years of age and older.

## Objective
Be the first player to knock out 3 of your opponent's creatures.

## Setup
Each player builds a deck of exactly 30 cards.
Each player places 1 creature face up as their Active Creature.
Each player may place up to 3 creatures on their Bench.
Each player draws 5 cards to start their hand.

## Turn Structure
1. Draw Phase: Draw 1 card from your deck.
2. Main Phase: You may do any of the following (in any order):
   - Play 1 creature to your bench
   - Play Action cards by paying their action point cost
   - Play Item cards by paying their action point cost
   - Attach Item cards to your active creature or a benched creature
3. Attack Phase: Use one attack from your active creature by paying its action point cost.
4. End Phase: End your turn. Action points reset to 3 next turn.

## Action Points
You start each turn with 3 action points.
Cards and attacks cost action points to use.
Unused action points do not carry over.

## Creatures
Your active creature is the one currently in battle.
You may switch your active creature with a benched one during your main phase.
If your active creature is knocked out, you must send a benched creature to the front.
If you have no creatures left, you lose.

## Card Types
- Creature Cards: Your main fighters. Each has HP, two attacks, and a passive ability.
- Action Cards: One use effects. Discard after playing.
- Item Cards: Attach to creatures for ongoing bonuses.
- Reaction Cards: Played during the opponent turn when a specific condition is met.
- Field Cards: Change the rules for both players while active.

## Status Effects
- Stressed: Next attack costs 1 extra action point.
- Confused: Flip a coin before attacking. Tails means the attack misses.
- Burnout: Take 10 damage at the end of your turn.
- Muted: Passive ability disabled for 1 turn.
- Petty: Next attack deals 10 extra damage.
- Stunned: Skip your next attack phase.

## Winning
The first player to score 3 knockouts wins.

## Rarity
- Common: Baseline cards available in every deck.
- Uncommon: Stronger cards with more complex effects.
- Rare: Powerful cards that can shift the game.
- Legendary: Once per game level threats that define strategies.

## Tips
Manage your action points carefully.
Status effects stack against you. Counter them fast.
Reaction cards are your safety net. Hold them for the right moment.
Field cards affect both players. Plan before you play them.
Your bench is your backup plan. Keep it stocked.
`;
